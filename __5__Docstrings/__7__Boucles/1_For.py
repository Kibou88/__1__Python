# Boucle For
# - Apprendre l'utilisation de la boucle FOR
############################################
liste = [1,4,3,5,6,8]

for element in liste:
    print(element)

# For-Else
for element in liste:
    if element == 10:
        print("10 est dans la liste")
else:
    print("10 n'est pas dans la liste")